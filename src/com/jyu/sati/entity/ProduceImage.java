package com.jyu.sati.entity;

/**
 * 产品成果图片实体类
 * 
 * @author 淋雨又调皮
 *
 */
public class ProduceImage {

	public final static int IMAGE_TYPE_GENERAL = -1;// 普通照片
	public final static int IMAGE_TYPE_SHOW_IN_HOMEPAGE = 1;// 要在首页展示的图片

	public final static int PRODUCE_IMAGE = 111;// 用于上传文件类型

	private Integer produceImageId;
	private String produceImageDesc;// 图片描述
	private String produceImageUrl;// 图片url
	private Integer produceImageType;// 图片类型
	private Integer produceId;// 绑定的产品id

	public Integer getProduceImageId() {
		return produceImageId;
	}

	public void setProduceImageId(Integer produceImageId) {
		this.produceImageId = produceImageId;
	}

	public String getProduceImageDesc() {
		return produceImageDesc;
	}

	public void setProduceImageDesc(String produceImageDesc) {
		this.produceImageDesc = produceImageDesc;
	}

	public String getProduceImageUrl() {
		return produceImageUrl;
	}

	public void setProduceImageUrl(String produceImageUrl) {
		this.produceImageUrl = produceImageUrl;
	}

	public Integer getProduceImageType() {
		return produceImageType;
	}

	public void setProduceImageType(Integer produceImageType) {
		this.produceImageType = produceImageType;
	}

	public Integer getProduceId() {
		return produceId;
	}

	public void setProduceId(Integer produceId) {
		this.produceId = produceId;
	}

	@Override
	public String toString() {
		return "ProduceImage [produceImageId=" + produceImageId + ", produceImageDesc=" + produceImageDesc
				+ ", produceImageUrl=" + produceImageUrl + ", produceImageType=" + produceImageType + ", produceId="
				+ produceId + "]";
	}

}