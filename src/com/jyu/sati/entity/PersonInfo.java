package com.jyu.sati.entity;

import java.util.Date;

/**
 * 个人基本信息
 * 
 * @author 淋雨又调皮
 *
 */
public class PersonInfo {

	private Integer pbiId;// 个人基本信息ID
	private String personName;// 姓名
	private String idcardNumber;// 身份证号码
	private Integer idcardImageId;// 身份证正面图片
	private Integer sex;// 性别
	private String nation;// 民族
	private Date birth;// 出生日期
	private Integer educationId;// 学历
	private String workplace;// 工作单位
	private String business;// 职务
	private Integer personImageId;// 个人照片
	private String personalProfile;// 个人简介
	private String remark;// 备注
	private Integer contactId;// 联系人ID

	public Integer getPbiId() {
		return pbiId;
	}

	public void setPbiId(Integer pbiId) {
		this.pbiId = pbiId;
	}

	public String getPersonName() {
		return personName;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}

	public String getIdcardNumber() {
		return idcardNumber;
	}

	public void setIdcardNumber(String idcardNumber) {
		this.idcardNumber = idcardNumber;
	}

	public Integer getIdcardImageId() {
		return idcardImageId;
	}

	public void setIdcardImageId(Integer idcardImageId) {
		this.idcardImageId = idcardImageId;
	}

	public Integer getSex() {
		return sex;
	}

	public void setSex(Integer sex) {
		this.sex = sex;
	}

	public String getNation() {
		return nation;
	}

	public void setNation(String nation) {
		this.nation = nation;
	}

	public Date getBirth() {
		return birth;
	}

	public void setBirth(Date birth) {
		this.birth = birth;
	}

	public Integer getEducationId() {
		return educationId;
	}

	public void setEducationId(Integer educationId) {
		this.educationId = educationId;
	}

	public String getWorkplace() {
		return workplace;
	}

	public void setWorkplace(String workplace) {
		this.workplace = workplace;
	}

	public String getBusiness() {
		return business;
	}

	public void setBusiness(String business) {
		this.business = business;
	}

	public Integer getPersonImageId() {
		return personImageId;
	}

	public void setPersonImageId(Integer personImageId) {
		this.personImageId = personImageId;
	}

	public String getPersonalProfile() {
		return personalProfile;
	}

	public void setPersonalProfile(String personalProfile) {
		this.personalProfile = personalProfile;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Integer getContactId() {
		return contactId;
	}

	public void setContactId(Integer contactId) {
		this.contactId = contactId;
	}

	@Override
	public String toString() {
		return "PersonInfo [pbiId=" + pbiId + ", personName=" + personName + ", idcardNumber=" + idcardNumber
				+ ", idcardImageId=" + idcardImageId + ", sex=" + sex + ", nation=" + nation + ", birth=" + birth
				+ ", educationId=" + educationId + ", workplace=" + workplace + ", business=" + business
				+ ", personImageId=" + personImageId + ", personalProfile=" + personalProfile + ", remark=" + remark
				+ ", contactId=" + contactId + "]";
	}
	
	
}