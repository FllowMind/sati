package com.jyu.sati.entity;

public class CategoryInfo {
    private Integer categoryInfoId;

    private String categoryInfoName;

    private Integer fatherId;

    private Integer categoryInfoLever;

    private Integer categoryInfoType;

    private Integer categoryInfoLevel;

    private Long fatherCategoryInfoId;

    public Integer getCategoryInfoId() {
        return categoryInfoId;
    }

    public void setCategoryInfoId(Integer categoryInfoId) {
        this.categoryInfoId = categoryInfoId;
    }

    public String getCategoryInfoName() {
        return categoryInfoName;
    }

    public void setCategoryInfoName(String categoryInfoName) {
        this.categoryInfoName = categoryInfoName;
    }

    public Integer getFatherId() {
        return fatherId;
    }

    public void setFatherId(Integer fatherId) {
        this.fatherId = fatherId;
    }

    public Integer getCategoryInfoLever() {
        return categoryInfoLever;
    }

    public void setCategoryInfoLever(Integer categoryInfoLever) {
        this.categoryInfoLever = categoryInfoLever;
    }

    public Integer getCategoryInfoType() {
        return categoryInfoType;
    }

    public void setCategoryInfoType(Integer categoryInfoType) {
        this.categoryInfoType = categoryInfoType;
    }

    public Integer getCategoryInfoLevel() {
        return categoryInfoLevel;
    }

    public void setCategoryInfoLevel(Integer categoryInfoLevel) {
        this.categoryInfoLevel = categoryInfoLevel;
    }

    public Long getFatherCategoryInfoId() {
        return fatherCategoryInfoId;
    }

    public void setFatherCategoryInfoId(Long fatherCategoryInfoId) {
        this.fatherCategoryInfoId = fatherCategoryInfoId;
    }

	@Override
	public String toString() {
		return "CategoryInfo [categoryInfoId=" + categoryInfoId + ", categoryInfoName=" + categoryInfoName
				+ ", fatherId=" + fatherId + ", categoryInfoLever=" + categoryInfoLever + ", categoryInfoType="
				+ categoryInfoType + ", categoryInfoLevel=" + categoryInfoLevel + ", fatherCategoryInfoId="
				+ fatherCategoryInfoId + "]";
	}
    
}