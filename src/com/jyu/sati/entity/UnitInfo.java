package com.jyu.sati.entity;

/**
 * 高校单位或科研单位实体类
 * 
 * @author 淋雨又调皮
 *
 */
public class UnitInfo {

	public final static int UNIT_TYPE_COLLEAGE = 1;// 高校单位
	public final static int UNIT_TYPE_SCIENTIFIC = 2;// 科研单位

	private Integer unitInfoId;// 单位id

	private Integer soeId;// 单位从业人员id

	private Integer baseInfoId;// 基本信息id

	private Integer unitType;// 单位类型

	public Integer getUnitInfoId() {
		return unitInfoId;
	}

	public void setUnitInfoId(Integer unitInfoId) {
		this.unitInfoId = unitInfoId;
	}

	public Integer getSoeId() {
		return soeId;
	}

	public void setSoeId(Integer soeId) {
		this.soeId = soeId;
	}

	public Integer getBaseInfoId() {
		return baseInfoId;
	}

	public void setBaseInfoId(Integer baseInfoId) {
		this.baseInfoId = baseInfoId;
	}

	public Integer getUnitType() {
		return unitType;
	}

	public void setUnitType(Integer unitType) {
		this.unitType = unitType;
	}

	@Override
	public String toString() {
		return "UnitInfo [unitInfoId=" + unitInfoId + ", soeId=" + soeId + ", baseInfoId=" + baseInfoId + ", unitType="
				+ unitType + "]";
	}

}